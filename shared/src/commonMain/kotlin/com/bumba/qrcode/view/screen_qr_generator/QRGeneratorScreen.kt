import com.bumba.qrcode.view.icon.IconRefresh

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun QRGeneratorScreen(
    viewModel: QRGeneratorViewModel
) {
    val state = viewModel.state.value

    var inputText by rememberSaveable { mutableStateOf("") }
    var isInputTextError by rememberSaveable { mutableStateOf(false) }
    var isQRCodeGenerated by rememberSaveable { mutableStateOf(false) }

    Scaffold(
        topBar = {
            ScreenTopBar(isNavigationEnable = isQRCodeGenerated) {
                isQRCodeGenerated = false
            }
        }
    ) {
        Column(
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally,
            modifier = Modifier.fillMaxSize()
                .verticalScroll(rememberScrollState())
                .padding(all = 16.dp),
        ) {
            if (isQRCodeGenerated) {
                inputText = ""
                Box(
                    contentAlignment = Alignment.Center,
                    modifier = Modifier
                        .clip(RoundedCornerShape(7.dp))
                        .border(BorderStroke(3.dp, Color.Black))
                        .size(300.dp)
                ) {
                    when (state) {
                        is QRGeneratorState.Loading -> {
                            CircularProgressIndicator(
                                color = MaterialTheme.colorScheme.primary,
                                strokeWidth = 25.dp,
                                modifier = Modifier.size(200.dp)
                                    .clip(RoundedCornerShape(10.dp))
                                    .animateContentSize()
                            )
                        }
                        is QRGeneratorState.Error -> {
                            IconButton(onClick = {
                                viewModel.onGenerateQRCode(inputText)
                            }) {
                                Icon(
                                    imageVector = IconRefresh,
                                    contentDescription = "Regenerate",
                                    modifier = Modifier.size(150.dp),
                                    tint = Color.Red
                                )
                            }
                        }
                        is QRGeneratorState.Success -> {
                            Image(
                                bitmap = state.qrCode,
                                contentScale = ContentScale.Fit,
                                contentDescription = "QR Code",
                                modifier = Modifier
                                    .fillMaxSize(if (getPlatform() == Platform.IOS) .8f else 1f)
                            )
                        }
                    }
                }
            } else {
                Text(
                    text = "Gerar QR Code",
                    style = MaterialTheme.typography.headlineLarge,
                    fontWeight = FontWeight.Bold
                )
                Spacer(modifier = Modifier.height(32.dp))
                OutlinedTextField(
                    value = inputText,
                    onValueChange = {
                        inputText = it
                        isInputTextError = inputText.isBlank()
                    },
                    label = { Text("texto", style = MaterialTheme.typography.titleMedium) },
                    textStyle = MaterialTheme.typography.titleMedium,
                    singleLine = false,
                    supportingText = {
                        if (isInputTextError) {
                            Text(
                                modifier = Modifier.fillMaxWidth(),
                                text = "Text input is too short",
                                textAlign = TextAlign.Start,
                                color = Color.Red
                            )
                        }
                    },
                    isError = isInputTextError,
                    keyboardActions = KeyboardActions(onGo = {
                        if (inputText.isBlank()) {
                            isInputTextError = inputText.isBlank()
                            return@KeyboardActions
                        }
                        viewModel.onGenerateQRCode(inputText)
                        isQRCodeGenerated = true
                    }),
                    keyboardOptions = KeyboardOptions.Default.copy(imeAction = ImeAction.Go),
                    modifier = Modifier.fillMaxWidth(1f)
                )
                Spacer(modifier = Modifier.height(20.dp))
                Button(
                    onClick = {
                        if (inputText.isBlank()) {
                            isInputTextError = inputText.isBlank()
                            return@Button
                        }
                        viewModel.onGenerateQRCode(inputText)
                        isQRCodeGenerated = true
                    },
                    shape = RoundedCornerShape(10.dp),
                    modifier = Modifier.height(50.dp).width(150.dp)
                ) {
                    Text(
                        text = "Generate",
                        style = MaterialTheme.typography.titleMedium
                    )
                }
            }
        }
    }
}


@OptIn(ExperimentalMaterial3Api::class)
@Composable
private fun ScreenTopBar(
    isNavigationEnable: Boolean,
    onNavigate: () -> Unit
) {
    TopAppBar(
        title = {
            Text(
                text = "QR Code",
                style = MaterialTheme.typography.titleLarge,
                fontWeight = FontWeight.Bold
            )
        },
        navigationIcon = {
            AnimatedVisibility(
                visible = isNavigationEnable,
                enter = slideInHorizontally(),
                exit = shrinkOut()
            ) {
                IconButton(
                    onClick = onNavigate,
                    modifier = Modifier.animateContentSize()
                ) {
                    Icon(
                        imageVector = IconCustomArrowBack,
                        contentDescription = null
                    )
                }
            }
        }
    )
}